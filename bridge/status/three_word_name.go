func getThreeWordName(pubkey string) string {
	mt := NewMT(uint32(numericHash(pubkey)))

	idx1 := mt.NextMax(int32(len(adj) - 1))
	idx2 := mt.NextMax(int32(len(adj) - 1))
	idx3 := mt.NextMax(int32(len(animals) - 1))

	return fmt.Sprintf("%s %s %s", adj[idx1], adj[idx2], animals[idx3])
}

// weird numeric hashe for our pubkey string

func numericHash(pubKey string) int64 {
	var seed float64 = 0
	for range pubKey {
		var hash float64 = 0
		for _, c := range pubKey {
			// WHAT. THE. HELL
			hash = float64(int32(c)) + float64(int32(int64(hash))<<6) + float64((int32(int64(hash)) << 16)) - hash
		}
		seed += hash
	}
	return int64(seed)
}

// from https://github.com/opencoff/go-mtrand/blob/master/mtrand.go

type MT struct {
	mt [624]uint32
	i  int
}

func NewMT(seed uint32) *MT {
	m := &MT{}
	//mt := m.mt[:]

	if seed == 0 {
		seed = uint32(time.Now().UnixNano())
	}

	m.mt[0] = (seed >> 0)

	for i := 1; i < 624; i++ {
		y := m.mt[i-1]
		y ^= (y >> 30)
		m.mt[i] = (1812433253 * y) + uint32(i)

	}

	m.i = 624 // compatibility with JS

	return m
}

func (m *MT) twist() {
	mt := m.mt[:]

	for i := 0; i < 624; i++ {
		y := (mt[i] & 0x80000000) + (mt[(i+1)%624] & 0x7fffffff)
		mt[i] = mt[(i+397)%624] ^ (y >> 1)

		if (y & 1) != 0 {
			mt[i] ^= 0x9908b0df
		}
	}
	m.i = 0
}

func (m *MT) Next() uint32 {
	mt := m.mt[:]
	i := m.i

	if i >= 624 {
		i = 0
		m.twist()
	}

	y := mt[i]
	y ^= (y >> 11)
	y ^= ((y << 7) & 2636928640)
	y ^= ((y << 15) & 4022730752)
	y ^= (y >> 18)

	m.i = i + 1
	return y
}

func (m *MT) NextF() float64 {
	value := m.Next()
	result := float64(value) / 4294967294.0
	return result
}

func (m *MT) NextMax(max int32) int32 {
	result := int32(m.NextF() * float64(max+1))
	return result
}

